package org.udec.util.enumerations;

/**
 * Enumeración que representa los diferentes estados del juego.
 * - MENU: El juego está en el menú principal.
 * - JUGANDO: El juego está en progreso.
 * - VICTORIA: El jugador ha ganado el juego.
 * - DERROTA: El jugador ha perdido el juego.
 */
public enum EstadoJuego {
    MENU,
    JUGANDO,
    VICTORIA,
    DERROTA;
}
